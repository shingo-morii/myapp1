def add(a, b):
    """
    2つの数値を加算して返します。

    Args:
        a (int or float): 1つ目の数値
        b (int or float): 2つ目の数値

    Returns:
        int or float: aとbの合計
    """
    return a + b

def subtract(a, b):
    """
    2つの数値を減算して返します。

    Args:
        a (int or float): 1つ目の数値
        b (int or float): 2つ目の数値

    Returns:
        int or float: aからbを引いた値
    """
    return a - b

def multiply(a, b):
    """
    2つの数値を掛け算して返します。

    Args:
        a (int or float): 1つ目の数値
        b (int or float): 2つ目の数値

    Returns:
        int or float: aとbの積
    """
    return a * b
